package sample_csv_project;

public class TranslationRule {

	private String name;
	private String inherit;
	private String translate;
	private String updateDestinationLanguage;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getInherit() {
		return inherit;
	}
	public void setInherit(String inherit) {
		this.inherit = inherit;
	}
	public String getTranslate() {
		return translate;
	}
	public void setTranslate(String translate) {
		this.translate = translate;
	}
	public String getUpdateDestinationLanguage() {
		return updateDestinationLanguage;
	}
	public void setUpdateDestinationLanguage(String updateDestinationLanguage) {
		this.updateDestinationLanguage = updateDestinationLanguage;
	}
	
	
	
	
}
